
package datatype;

public class LoopDemo {
    public static void main(String[] args) {
        int i;
        
        for(i=1;i<=10;i++)
        {
            System.out.println(i+" Bangladesh");
        }
        
        int x;
        x=1;
        while(x<=10)
        {
            System.out.println(x+" Bangladesh");
            x++;
        }
        
        int a;
        a=1;
        do
         {
             System.out.println(a+" Bangladesh");
             a++;
         }
         while(a<=10);
    }
}
